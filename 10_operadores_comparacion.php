
<?php

$valor_1 = 1;

var_dump($valor_1 === "1"); // identico
echo "<br>";

var_dump($valor_1 != "1"); // diferente
echo "<br>";

var_dump($valor_1 <> "1"); // diferente
echo "<br>";

var_dump($valor_1 !== "1"); // no identico
echo "<br>";

var_dump($valor_1 < 1); // menor que 
echo "<br>";

var_dump($valor_1 > 1); // mayor que 
echo "<br>";

var_dump($valor_1 <= 1); // menor o igual 
echo "<br>";

var_dump($valor_1 >= 1); // mayor o igual 
echo "<br>";

